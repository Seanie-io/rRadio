-- Function to reformat the country name and get the translated country name
function CountryTranslations:GetCountryName(lang, country_key)
    -- Reformat the country name (e.g., "the_united_kingdom" -> "The United Kingdom")
    local formattedName = country_key:gsub("_", " "):gsub("(%a)([%w_']*)", function(a, b) return string.upper(a) .. string.lower(b) end)

    -- Debug statement to show the reformatting process
    print(string.format("Reformatted name %s to %s", country_key, formattedName))

    -- Return the translated name or the formatted name if no translation is found
    local translatedName = self[lang] and self[lang][formattedName] or formattedName

    -- Debug statement to show the translation process
    print(string.format("Translating name %s to %s", formattedName, translatedName))

    return translatedName
end

return CountryTranslations
